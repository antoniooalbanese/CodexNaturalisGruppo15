package application.model;

/**
 * Questo enumerativo descrive i 4 possibili regni di cui fanno
 * parte le carte(risorsa o oro) e le risorse del gioco. 
 */
public enum Regno {
	/**
	 * Regno vegetale
	 */
	VEGETALE,
	/**
	 * Regno animale
	 */
	ANIMALE, 
	/**
	 * Regno funghi
	 */
	FUNGHI,
	/**
	 * Regno insetti
	 */
	INSETTI
}
